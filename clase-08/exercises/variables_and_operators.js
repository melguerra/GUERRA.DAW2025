// 1. Variables y Operadores

// a. Suma de dos variables numéricas
var creditosAprobados = 60;
var creditosRestantes = 20;
var totalCreditos = creditosAprobados + creditosRestantes;
alert("Total de créditos en carrera: " + totalCreditos);

// b. Concatenación de dos variables string
var facultad = "UAI ";
var carrera = "Ingeniería Informática";
var descripcionCarrera = facultad + carrera;
alert("Carrera: " + descripcionCarrera);

// c. Sumar largos de dos strings
var materia1 = "Programación Orientada a Objetos";
var materia2 = "Diseño y Arquitectura de Software";
var sumaCaracteres = materia1.length + materia2.length;
alert("Total de caracteres en nombres de materias: " + sumaCaracteres);
